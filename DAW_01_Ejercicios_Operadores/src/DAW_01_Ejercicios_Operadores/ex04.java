package DAW_01_Ejercicios_Operadores;

public class ex04 {

	public static void main(String[] args) {
		int circ1=2;
		double pi=3.1416;
		System.out.println("El radi de la circumferència es: " + circ1);
		System.out.println("La longuitud de la circumferència es: " + 2*pi*circ1);
		System.out.println("El area de la circumferència es: " + pi*circ1 + Math.pow(circ1,2));
	}

}
