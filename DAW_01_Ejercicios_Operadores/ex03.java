package DAW_01_Ejercicios_Operadores;

public class ex03 {

	public static void main(String[] args) {
		int grados_celsius=37;
		double grados_fahrenheit;
		System.out.println("Los grados iniciales son:" + grados_celsius);
		grados_fahrenheit=grados_celsius*1.8000+32;
		System.out.println("Los grados en Farenheit son: " + grados_fahrenheit);		
	}

}
